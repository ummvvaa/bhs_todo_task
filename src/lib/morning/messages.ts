// Меняйте тексты в этом файле, логику трогать не нужно.

export const morningMessages = {
  /**
   * Утреннее приветствие, когда на сегодня есть назначенные задачи.
   * name — имя сотрудника (или пусто); taskList — готовый список строк задач.
   */
  greetingWithTasks: (name: string, taskList: string): string =>
    `Доброе утро${name ? `, ${name}` : ''}! Ваши задачи на сегодня:\n${taskList}\n\n` +
    'Что планируете сделать сегодня? Можете дописать задачи ответом — я добавлю их вам.',

  /** Утреннее приветствие, когда на сегодня задач нет (короткое, без списка). */
  greetingNoTasks: (name: string): string =>
    `Доброе утро${name ? `, ${name}` : ''}! На сегодня назначенных задач нет.\n\n` +
    'Что планируете сделать сегодня? Можете написать задачи ответом — я добавлю их вам.',

  /** Одна строка списка задач. dueText — дедлайн в поясе Алматы. */
  taskLine: (title: string, dueText: string): string => `• ${title} (срок ${dueText})`,

  /** Подтверждение после того, как ответ сотрудника разобран в задачи. */
  tasksCreated: (titles: string[]): string =>
    `Добавил вам на сегодня ${titles.length === 1 ? 'задачу' : 'задачи'}:\n` +
    titles.map((t) => `• ${t}`).join('\n'),

  /** Ответ, когда в сообщении не нашлось конкретных задач. */
  nothingParsed:
    'Не нашёл в ответе конкретных задач. Напишите по пункту на строку, например: ' +
    '«Подготовить отчёт, позвонить родителям 9А».',

  /** Фолбэк, когда ИИ недоступен (лимит/ошибка Groq) или сбой сохранения. */
  parseFallback:
    'Не удалось разобрать ответ — ИИ временно недоступен. Попробуйте позже или добавьте задачи в приложении.',
}
